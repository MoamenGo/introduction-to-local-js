// Write 5 examples of expressions using each of the operations: +, -, *, /, and %. Print out the result of your expressions using console.log. Be sure to test your work by running your code!

// Addition
console.log(5 + 3);       // Output: 8
console.log(-2 + 7);      // Output: 5
console.log(10.5 + 2.5);  // Output: 13
console.log(0 + 0);       // Output: 0
console.log(100 + (-50)); // Output: 50

// Subtraction
console.log(10 - 4);      // Output: 6
console.log(3 - 8);       // Output: -5
console.log(15.5 - 7.2);  // Output: 8.3
console.log(0 - 0);       // Output: 0
console.log(-20 - (-10)); // Output: -10

// Multiplication
console.log(3 * 4);       // Output: 12
console.log(-2 * 6);      // Output: -12
console.log(2.5 * 8);     // Output: 20
console.log(0 * 100);     // Output: 0
console.log(7 * (-3));    // Output: -21

// Division
console.log(15 / 3);      // Output: 5
console.log(8 / 2);       // Output: 4
console.log(10.5 / 2.5);  // Output: 4.2
console.log(0 / 100);     // Output: 0
console.log(-14 / (-2));  // Output: 7

// Modulo
console.log(10 % 3);      // Output: 1
console.log(17 % 5);      // Output: 2
console.log(25.5 % 5);    // Output: 0.5
console.log(8 % 2);       // Output: 0
console.log(-15 % 4);     // Output: -3


